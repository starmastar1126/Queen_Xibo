
    	<h2>Types of Content</h2>
    
  		<p>A content item is piece of media that you wish to display. This can be different types of media, such as 
        Videos, Flash animations, RSS feeds, Pictures and more.</p>
		<p>The library table comprises of all the content items that you have uploaded or created. You can also see any other 
        content items that a user has shared within this table</p>

